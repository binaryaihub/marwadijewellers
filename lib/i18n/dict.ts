export const dict: Record<Lang, typeof en> = { en, hi };
export type DictKey = keyof typeof en;

export function translate(lang: Lang, key: DictKey, vars?: Record<string, string | number>): string {
  let raw: string | undefined = dict[lang]?.[key] ?? dict.en[key];
  if (raw === undefined) {
    // Graceful fallback for unknown subcategory slugs from imported data:
    // "sub.foo-bar" → "foo bar".
    if (typeof key === "string" && key.startsWith("sub.")) {
      raw = key.slice(4).replace(/-/g, " ");
    } else {
      raw = key as string;
    }
  }
  if (!vars) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`));
}
